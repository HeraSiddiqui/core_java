import java.util.*;
class Inherit
{
 class A   //PARENT CLASS
 {
	 void m1()  {}
	 void m2()  {}
 }

 class B extends A  // CHILD CLASS OR DERIVED CLASS
 {
	 void m3()   {}
	 void m4()   {}
 }

 class C extends B    
 {
	  void m5()  {}
	  void m6()  {}
 }

	public static void main(String args[])
	{
		System.out.println("HELLO WORLD");
	}

}

               